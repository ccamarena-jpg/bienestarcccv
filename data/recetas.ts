// Rotación por día de semana
const ROTACION_DIA: Record<Categoria, number[]> = {
  desayuno: [0, 1, 2, 3, 4, 5, 6], // índice por día (Dom=0, Lun=1, ..., Sáb=6)
  almuerzo: [0, 1, 2, 3, 4, 5, 6],
  cena:     [0, 1, 2, 3, 4, 5, 6],
  snack:    [0, 1, 2, 3, 4, 5, 6],
};

export function getRecetaDelDia(categoria: Categoria): Receta {
  const opciones = RECETAS.filter((r) => r.categoria === categoria);
  const diaSemana = new Date().getDay(); // 0=Dom, 6=Sáb
  return opciones[diaSemana % opciones.length];
}

export function getRecetasPorCategoria(categoria: Categoria): Receta[] {
  return RECETAS.filter((r) => r.categoria === categoria);
}
